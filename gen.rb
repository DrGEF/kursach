# accountant
def acc amount = 20
	@names = []
	amount.downto(1) do |a| 
		@names << FNAME[rand(FNAME.length)] + ' ' + LNAME[rand(LNAME.length)]
	end

	id = 0

	@names.uniq!
	f = File.open('acc.txt', 'w')
		@names.each do |n|
			id = id + 1
			f.puts id.to_s + T + n
		end
	f.close
end

