# employees
def emp amount = 200
	@names = []
	amount.downto(1) do |a|
		@names << FNAME[rand(FNAME.length)] + ' ' + LNAME[rand(LNAME.length)]
	end
	id = 0
	f = File.open('emp.txt', 'w')
		@names.each do |n|
			id = id + 1
			f.puts id.to_s + T + n + T + rand(15000..50000).to_s + T + rand(1..40).to_s
		end
	f.close
end

# company
def cmp
	@dateE = Date.new(2000,12,31)
	@dateS = Date.new(2000,1,1)

	f = File.open('cmp.txt', 'w')
	40.downto(1) do |c|
		@dateE.downto(@dateS) do |d|
			f.puts c.to_s + T + d.to_s + T + rand(100000).to_s + T + rand(150000).to_s
		end
	end
	f.close
end

# debt
def deb amount=35
	f = File.open('deb.txt', 'w')
		amount.downto(1) do |a|
			id = rand(1..40).to_s
			dst = rand(1..40).to_s
			dst = rand(1..40).to_s if id == dst
			f.puts id + T + rand(1000..200000).to_s + T + dst
		end
	f.close
end